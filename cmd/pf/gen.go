func newGenCmd() *cobra.Command {
	var protoDir, outDir, lang string
	var includeBuiltin bool
	var expandBuiltinHeads bool
	cmd := &cobra.Command{
		Use:   "gen",
		Short: "Generate struct definitions from PDL",
		RunE: func(cmd *cobra.Command, args []string) error {
			protos, structs, err := loadPDLForGen(protoDir, includeBuiltin)
			if err != nil {
				return err
			}
			if len(protos) == 0 && len(structs) == 0 {
				return fmt.Errorf("no protocol/struct definitions found in %q", protoDir)
			}

			if err := os.MkdirAll(outDir, 0o755); err != nil {
				return fmt.Errorf("create output dir: %w", err)
			}

			targets := []string{strings.ToLower(lang)}
			if strings.EqualFold(lang, "all") {
				targets = []string{"go", "c", "cpp"}
			}
			for _, t := range targets {
				var content, name string
				switch t {
				case "go":
					name = "pdl_gen.go"
					content = renderGo(protos, structs, expandBuiltinHeads)
				case "c":
					name = "pdl_gen.h"
					content = renderC(protos, structs, expandBuiltinHeads)
				case "cpp":
					name = "pdl_gen.hpp"
					content = renderCPP(protos, structs, expandBuiltinHeads)
					outPath := filepath.Join(outDir, name)
					if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
						return fmt.Errorf("write %s: %w", outPath, err)
					}
					fmt.Printf("generated: %s\n", outPath)
					codecName := "pdl_gen_codec.hpp"
					codecContent := renderCPPCodecFile(protos, structs, expandBuiltinHeads)
					codecPath := filepath.Join(outDir, codecName)
					if err := os.WriteFile(codecPath, []byte(codecContent), 0o644); err != nil {
						return fmt.Errorf("write %s: %w", codecPath, err)
					}
					fmt.Printf("generated: %s\n", codecPath)
					continue
				default:
					return fmt.Errorf("unsupported lang %q, expected one of: go,c,cpp,all", t)
				}
				outPath := filepath.Join(outDir, name)
				if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
					return fmt.Errorf("write %s: %w", outPath, err)
				}
				fmt.Printf("generated: %s\n", outPath)
			}
			return nil
		},
	}
	cmd.Flags().StringVarP(&protoDir, "proto", "p", "proto", "Protocol definition directory (.pdl files)")
	cmd.Flags().StringVarP(&outDir, "out", "o", "generated", "Output directory")
	cmd.Flags().StringVar(&lang, "lang", "go", "Target language: go|c|cpp|all")
	cmd.Flags().BoolVarP(&includeBuiltin, "builtin-proto", "b", false, "Include built-in protocol definitions")
	cmd.Flags().BoolVar(&expandBuiltinHeads, "expand-builtin-heads", false, "Expand builtin head fields (mac/ipv4/ipv6) to raw arrays instead of named types")
	return cmd
}

func loadPDLForGen(dir string, includeBuiltin bool) ([]*pdl.Protocol, []*pdl.Struct, error) {
	reg := pdl.NewRegistry()
	if includeBuiltin {
		if err := reg.LoadBuiltinCommonProtocols(); err != nil {
			return nil, nil, fmt.Errorf("load builtin protocols: %w", err)
		}
	}
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, nil, fmt.Errorf("read proto dir %q: %w", dir, err)
	}

	protoByName := map[string]*pdl.Protocol{}
	structByName := map[string]*pdl.Struct{}
	for _, e := range entries {
		if e.IsDir() || filepath.Ext(e.Name()) != ".pdl" {
			continue
		}
		path := filepath.Join(dir, e.Name())
		data, err := os.ReadFile(path)
		if err != nil {
			return nil, nil, fmt.Errorf("read %s: %w", path, err)
		}
		ps := pdl.NewParser(string(data))
		protos, structs, err := ps.ParseFile()
		if err != nil {
			return nil, nil, fmt.Errorf("parse %s: %w", path, err)
		}
		for _, st := range structs {
			structByName[strings.ToLower(st.Name)] = st
		}
		for _, pr := range protos {
			protoByName[strings.ToLower(pr.Name)] = pr
		}
	}
	if includeBuiltin {
		for _, name := range reg.List() {
			if _, ok := protoByName[strings.ToLower(name)]; ok {
				continue
			}
			if p := reg.Get(name); p != nil {
				protoByName[strings.ToLower(name)] = p
			}
		}
	}

	protoNames := make([]string, 0, len(protoByName))
	for k := range protoByName {
		protoNames = append(protoNames, k)
	}
	sort.Strings(protoNames)
	structNames := make([]string, 0, len(structByName))
	for k := range structByName {
		structNames = append(structNames, k)
	}
	sort.Strings(structNames)

	outProtos := make([]*pdl.Protocol, 0, len(protoNames))
	for _, k := range protoNames {
		outProtos = append(outProtos, protoByName[k])
	}
	outStructs := make([]*pdl.Struct, 0, len(structNames))
	for _, k := range structNames {
		outStructs = append(outStructs, structByName[k])
	}
	return outProtos, outStructs, nil
}

func renderGo(protos []*pdl.Protocol, structs []*pdl.Struct, expandBuiltinHeads bool) string {
	var b strings.Builder
	b.WriteString("// Code generated by packetforge pf gen; DO NOT EDIT.\n\n")
	b.WriteString("package generated\n\n")
	if !expandBuiltinHeads {
		b.WriteString("type Mac [6]byte\n")
		b.WriteString("type IPv4 [4]byte\n")
		b.WriteString("type IPv6 [16]byte\n\n")
	}
	for _, st := range structs {
		renderGoType(&b, st.Name, st.Fields, expandBuiltinHeads)
	}
	for _, pr := range protos {
		renderGoType(&b, pr.Name, pr.Fields, expandBuiltinHeads)
	}
	return b.String()
}

func renderGoType(b *strings.Builder, name string, fields []*pdl.Field, expandBuiltinHeads bool) {
	fmt.Fprintf(b, "type %s struct {\n", goName(name))
	for _, f := range fields {
		t, comment, ok := goFieldType(f, expandBuiltinHeads)
		if !ok {
			fmt.Fprintf(b, "\t// TODO unsupported field %s (%s)\n", f.Name, fieldDesc(f))
			continue
		}
		fmt.Fprintf(b, "\t%s %s `json:\"%s\"`\n", goName(f.Name), t, f.Name)
		if comment != "" {
			fmt.Fprintf(b, "\t// %s\n", comment)
		}
	}
	b.WriteString("}\n\n")
}

func renderC(protos []*pdl.Protocol, structs []*pdl.Struct, expandBuiltinHeads bool) string {
	var b strings.Builder
	b.WriteString("/* Code generated by packetforge pf gen; DO NOT EDIT. */\n")
	b.WriteString("#pragma once\n\n#include <stdint.h>\n#include <stddef.h>\n\n")
	if !expandBuiltinHeads {
		b.WriteString("typedef uint8_t Mac[6];\n")
		b.WriteString("typedef uint8_t IPv4[4];\n")
		b.WriteString("typedef uint8_t IPv6[16];\n\n")
	}
	for _, st := range structs {
		renderCType(&b, st.Name, st.Fields, expandBuiltinHeads)
	}
	for _, pr := range protos {
		renderCType(&b, pr.Name, pr.Fields, expandBuiltinHeads)
	}
	return b.String()
}

func renderCPP(protos []*pdl.Protocol, structs []*pdl.Struct, expandBuiltinHeads bool) string {
	var b strings.Builder
	b.WriteString("// Code generated by packetforge pf gen; DO NOT EDIT.\n")
	b.WriteString("#pragma once\n\n#include <array>\n#include <cstddef>\n#include <cstdint>\n#include <vector>\n\n")
	if !expandBuiltinHeads {
		b.WriteString("struct Mac { std::uint8_t bytes[6]{}; };\n")
		b.WriteString("struct IPv4 { std::uint8_t bytes[4]{}; };\n")
		b.WriteString("struct IPv6 { std::uint8_t bytes[16]{}; };\n\n")
	}
	for _, st := range structs {
		renderCPPType(&b, st.Name, st.Fields, expandBuiltinHeads)
	}
	for _, pr := range protos {
		renderCPPType(&b, pr.Name, pr.Fields, expandBuiltinHeads)
	}
	return b.String()
}

func renderCPPCodecFile(protos []*pdl.Protocol, structs []*pdl.Struct, expandBuiltinHeads bool) string {
	var b strings.Builder
	b.WriteString("// Code generated by packetforge pf gen; DO NOT EDIT.\n")
	b.WriteString("#pragma once\n\n#include \"pdl_gen.hpp\"\n")
	b.WriteString("#include <type_traits>\n\n")
	b.WriteString("// Usage example:\n")
	b.WriteString("//   Sisl msg{};\n")
	b.WriteString("//   msg.metric_count = 1;\n")
	b.WriteString("//   msg.metrics.push_back(Metric{});\n")
	b.WriteString("//   std::vector<std::uint8_t> bytes;\n")
	b.WriteString("//   if (!pdl_codec::Serialize(msg, bytes)) { /* handle error */ }\n")
	b.WriteString("//\n")
	b.WriteString("//   Sisl parsed{};\n")
	b.WriteString("//   std::size_t off = 0;\n")
	b.WriteString("//   if (!pdl_codec::Deserialize(bytes.data(), bytes.size(), off, parsed)) { /* handle error */ }\n")
	b.WriteString("//   if (off != bytes.size()) { /* trailing bytes */ }\n\n")
	defs := make([]cppDef, 0, len(structs)+len(protos))
	for _, st := range structs {
		defs = append(defs, cppDef{Name: st.Name, Fields: st.Fields})
	}
	for _, pr := range protos {
		defs = append(defs, cppDef{Name: pr.Name, Fields: pr.Fields})
	}
	renderCPPCodec(&b, defs, expandBuiltinHeads)
	return b.String()
}

func renderCType(b *strings.Builder, name string, fields []*pdl.Field, expandBuiltinHeads bool) {
	typeName := goName(name)
	fmt.Fprintf(b, "typedef struct %s {\n", typeName)
	wrote := false
	for _, f := range fields {
		t, comment, ok := cFieldType(f, expandBuiltinHeads)
		if !ok {
			fmt.Fprintf(b, "    /* TODO unsupported field %s (%s) */\n", f.Name, fieldDesc(f))
			continue
		}
		fmt.Fprintf(b, "    %s\n", cDecl(t, cName(f.Name)))
		if f.Array == pdl.ArrayDynamic {
			fmt.Fprintf(b, "    size_t %s_len;\n", cName(f.Name))
		}
		if comment != "" {
			fmt.Fprintf(b, "    /* %s */\n", comment)
		}
		wrote = true
	}
	if !wrote {
		b.WriteString("    uint8_t _reserved;\n")
	}
	fmt.Fprintf(b, "} %s;\n\n", typeName)
}

func renderCPPType(b *strings.Builder, name string, fields []*pdl.Field, expandBuiltinHeads bool) {
	typeName := goName(name)
	fmt.Fprintf(b, "struct %s {\n", typeName)
	wrote := false
	for _, f := range fields {
		t, comment, ok := cppFieldType(f, expandBuiltinHeads)
		if !ok {
			fmt.Fprintf(b, "    // TODO unsupported field %s (%s)\n", f.Name, fieldDesc(f))
			continue
		}
		fmt.Fprintf(b, "    %s\n", cppDecl(t, cName(f.Name)))
		if comment != "" {
			fmt.Fprintf(b, "    // %s\n", comment)
		}
		wrote = true
	}
	if !wrote {
		b.WriteString("    std::uint8_t _reserved{};\n")
	}
	b.WriteString("};\n\n")
}

func goFieldType(f *pdl.Field, expandBuiltinHeads bool) (string, string, bool) {
	base, ok := goBaseType(f, expandBuiltinHeads)
	if !ok {
		return "", "", false
	}
	switch f.Array {
	case pdl.ArrayNone:
		return base, "", true
	case pdl.ArrayFixed:
		return fmt.Sprintf("[%d]%s", f.ArrayLen, base), "", true
	case pdl.ArrayDynamic:
		return "[]" + base, "dynamic array", true
	case pdl.ArrayField:
		return "[]" + base, "array length depends on field " + f.ArrayLenField, true
	default:
		return "", "", false
	}
}

func cFieldType(f *pdl.Field, expandBuiltinHeads bool) (string, string, bool) {
	if f.Array == pdl.ArrayNone {
		switch f.Type {
		case pdl.TypeMAC:
			if expandBuiltinHeads {
				return "uint8_t[6]", "", true
			}
			return "Mac", "", true
		case pdl.TypeIPv4:
			if expandBuiltinHeads {
				return "uint8_t[4]", "", true
			}
			return "IPv4", "", true
		case pdl.TypeIPv6:
			if expandBuiltinHeads {
				return "uint8_t[16]", "", true
			}
			return "IPv6", "", true
		}
	}
	base, ok := cBaseType(f, expandBuiltinHeads)
	if !ok {
		return "", "", false
	}
	switch f.Array {
	case pdl.ArrayNone:
		return base, "", true
	case pdl.ArrayFixed:
		return fmt.Sprintf("%s[%d]", base, f.ArrayLen), "", true
	case pdl.ArrayDynamic:
		return base + "*", "dynamic array, length field: " + cName(f.Name) + "_len", true
	case pdl.ArrayField:
		return base + "*", "array length depends on field " + f.ArrayLenField, true
	default:
		return "", "", false
	}
}

