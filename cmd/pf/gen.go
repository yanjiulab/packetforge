func fieldDesc(f *pdl.Field) string {
	switch f.Array {
	case pdl.ArrayDynamic:
		return "dynamic array"
	case pdl.ArrayField:
		return "array len by field " + f.ArrayLenField
	default:
		return "unsupported type"
	}
}

func goName(s string) string {
	parts := splitIdent(s)
	if len(parts) == 0 {
		return "X"
	}
	var out strings.Builder
	for _, p := range parts {
		out.WriteString(strings.ToUpper(p[:1]))
		if len(p) > 1 {
			out.WriteString(strings.ToLower(p[1:]))
		}
	}
	return out.String()
}

func cName(s string) string {
	parts := splitIdent(s)
	if len(parts) == 0 {
		return "x"
	}
	return strings.ToLower(strings.Join(parts, "_"))
}

func splitIdent(s string) []string {
	f := strings.FieldsFunc(s, func(r rune) bool {
		return !((r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9'))
	})
	if len(f) == 0 {
		return nil
	}
	return f
}

func cDecl(t, name string) string {
	if i := strings.Index(t, "["); i > 0 {
		return fmt.Sprintf("%s %s%s;", t[:i], name, t[i:])
	}
	return fmt.Sprintf("%s %s;", t, name)
}

func cppDecl(t, name string) string {
	if i := strings.Index(t, "["); i > 0 {
		return fmt.Sprintf("%s %s%s{};", t[:i], name, t[i:])
	}
	return fmt.Sprintf("%s %s{};", t, name)
}

type cppDef struct {
	Name   string
	Fields []*pdl.Field
}

func renderCPPCodec(b *strings.Builder, defs []cppDef, expandBuiltinHeads bool) {
	b.WriteString("namespace pdl_codec {\n\n")
	b.WriteString("inline void WriteU8(std::vector<std::uint8_t>& out, std::uint8_t v) { out.push_back(v); }\n")
	b.WriteString("inline void WriteU16(std::vector<std::uint8_t>& out, std::uint16_t v) { out.push_back(static_cast<std::uint8_t>((v >> 8) & 0xFF)); out.push_back(static_cast<std::uint8_t>(v & 0xFF)); }\n")
	b.WriteString("inline void WriteU32(std::vector<std::uint8_t>& out, std::uint32_t v) { for (int i = 3; i >= 0; --i) out.push_back(static_cast<std::uint8_t>((v >> (i * 8)) & 0xFF)); }\n")
	b.WriteString("inline void WriteU64(std::vector<std::uint8_t>& out, std::uint64_t v) { for (int i = 7; i >= 0; --i) out.push_back(static_cast<std::uint8_t>((v >> (i * 8)) & 0xFF)); }\n")
	b.WriteString("inline bool ReadU8(const std::uint8_t* data, std::size_t len, std::size_t& off, std::uint8_t& v) { if (off + 1 > len) return false; v = data[off++]; return true; }\n")
	b.WriteString("inline bool ReadU16(const std::uint8_t* data, std::size_t len, std::size_t& off, std::uint16_t& v) { if (off + 2 > len) return false; v = (static_cast<std::uint16_t>(data[off]) << 8) | data[off+1]; off += 2; return true; }\n")
	b.WriteString("inline bool ReadU32(const std::uint8_t* data, std::size_t len, std::size_t& off, std::uint32_t& v) { if (off + 4 > len) return false; v = (static_cast<std::uint32_t>(data[off]) << 24) | (static_cast<std::uint32_t>(data[off+1]) << 16) | (static_cast<std::uint32_t>(data[off+2]) << 8) | data[off+3]; off += 4; return true; }\n")
	b.WriteString("inline bool ReadU64(const std::uint8_t* data, std::size_t len, std::size_t& off, std::uint64_t& v) { if (off + 8 > len) return false; v = 0; for (int i = 0; i < 8; ++i) { v = (v << 8) | data[off+i]; } off += 8; return true; }\n")
	b.WriteString("inline void WriteBytes(std::vector<std::uint8_t>& out, const std::uint8_t* p, std::size_t n) { out.insert(out.end(), p, p + n); }\n")
	b.WriteString("inline bool ReadBytes(const std::uint8_t* data, std::size_t len, std::size_t& off, std::uint8_t* dst, std::size_t n) { if (off + n > len) return false; for (std::size_t i = 0; i < n; ++i) dst[i] = data[off+i]; off += n; return true; }\n\n")

	for _, d := range defs {
		fmt.Fprintf(b, "inline bool Serialize(const %s& in, std::vector<std::uint8_t>& out);\n", goName(d.Name))
		fmt.Fprintf(b, "inline bool Deserialize(const std::uint8_t* data, std::size_t len, std::size_t& off, %s& out);\n", goName(d.Name))
	}
	b.WriteString("\n")

	for _, d := range defs {
		typeName := goName(d.Name)
		fmt.Fprintf(b, "inline bool Serialize(const %s& in, std::vector<std::uint8_t>& out) {\n", typeName)
		for _, f := range d.Fields {
			field := "in." + cName(f.Name)
			b.WriteString(cppEmitSerializeField(f, field, expandBuiltinHeads, "in."))
		}
		b.WriteString("    return true;\n}\n\n")

		fmt.Fprintf(b, "inline bool Deserialize(const std::uint8_t* data, std::size_t len, std::size_t& off, %s& out) {\n", typeName)
		for i, f := range d.Fields {
			field := "out." + cName(f.Name)
			isLast := i == len(d.Fields)-1
			b.WriteString(cppEmitDeserializeField(f, field, expandBuiltinHeads, "out.", isLast))
		}
		b.WriteString("    return true;\n}\n\n")
	}
	b.WriteString("} // namespace pdl_codec\n")
}

func cppEmitSerializeField(f *pdl.Field, expr string, expandBuiltinHeads bool, root string) string {
	var b strings.Builder
	switch f.Array {
	case pdl.ArrayNone:
		b.WriteString(cppIndent(cppEmitSerializeElem(f, expr, expandBuiltinHeads), "    "))
	case pdl.ArrayFixed:
		b.WriteString(fmt.Sprintf("    for (std::size_t i = 0; i < %d; ++i) {\n", f.ArrayLen))
		b.WriteString(cppIndent(cppEmitSerializeElem(f, expr+"[i]", expandBuiltinHeads), "        "))
		b.WriteString("    }\n")
	case pdl.ArrayDynamic:
		b.WriteString(fmt.Sprintf("    for (const auto& elem : %s) {\n", expr))
		b.WriteString(cppIndent(cppEmitSerializeElem(f, "elem", expandBuiltinHeads), "        "))
		b.WriteString("    }\n")
	case pdl.ArrayField:
		b.WriteString(fmt.Sprintf("    if (static_cast<std::size_t>(%s%s) != %s.size()) return false;\n", root, cName(f.ArrayLenField), expr))
		b.WriteString(fmt.Sprintf("    for (const auto& elem : %s) {\n", expr))
		b.WriteString(cppIndent(cppEmitSerializeElem(f, "elem", expandBuiltinHeads), "        "))
		b.WriteString("    }\n")
	}
	return b.String()
}

func cppEmitDeserializeField(f *pdl.Field, expr string, expandBuiltinHeads bool, root string, isLast bool) string {
	var b strings.Builder
	switch f.Array {
	case pdl.ArrayNone:
		b.WriteString(cppIndent(cppEmitDeserializeElem(f, expr, expandBuiltinHeads), "    "))
	case pdl.ArrayFixed:
		b.WriteString(fmt.Sprintf("    for (std::size_t i = 0; i < %d; ++i) {\n", f.ArrayLen))
		b.WriteString(cppIndent(cppEmitDeserializeElem(f, expr+"[i]", expandBuiltinHeads), "        "))
		b.WriteString("    }\n")
	case pdl.ArrayDynamic:
		if !isLast {
			b.WriteString("    // ambiguous []T not at tail; cannot safely deserialize\n")
			b.WriteString("    return false;\n")
			return b.String()
		}
		b.WriteString(fmt.Sprintf("    %s.clear();\n", expr))
		b.WriteString("    while (off < len) {\n")
		b.WriteString("        typename std::decay<decltype(" + expr + ")>::type::value_type elem{};\n")
		b.WriteString(cppIndent(cppEmitDeserializeElem(f, "elem", expandBuiltinHeads), "        "))
		b.WriteString("        " + expr + ".push_back(elem);\n")
		b.WriteString("    }\n")
	case pdl.ArrayField:
		b.WriteString(fmt.Sprintf("    %s.clear();\n", expr))
		b.WriteString(fmt.Sprintf("    %s.reserve(static_cast<std::size_t>(%s%s));\n", expr, root, cName(f.ArrayLenField)))
		b.WriteString(fmt.Sprintf("    for (std::size_t i = 0; i < static_cast<std::size_t>(%s%s); ++i) {\n", root, cName(f.ArrayLenField)))
		b.WriteString("        typename std::decay<decltype(" + expr + ")>::type::value_type elem{};\n")
		b.WriteString(cppIndent(cppEmitDeserializeElem(f, "elem", expandBuiltinHeads), "        "))
		b.WriteString("        " + expr + ".push_back(elem);\n")
		b.WriteString("    }\n")
	}
	return b.String()
}

func cppEmitSerializeElem(f *pdl.Field, expr string, expandBuiltinHeads bool) string {
	switch f.Type {
	case pdl.TypeU8:
		return "WriteU8(out, " + expr + ");\n"
	case pdl.TypeU16:
		return "WriteU16(out, " + expr + ");\n"
	case pdl.TypeU32:
		return "WriteU32(out, " + expr + ");\n"
	case pdl.TypeU64:
		return "WriteU64(out, " + expr + ");\n"
	case pdl.TypeMAC:
		return fmt.Sprintf("WriteBytes(out, %s, 6);\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeIPv4:
		return fmt.Sprintf("WriteBytes(out, %s, 4);\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeIPv6:
		return fmt.Sprintf("WriteBytes(out, %s, 16);\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeStructRef, pdl.TypeStructArray:
		return "if (!Serialize(" + expr + ", out)) return false;\n"
	default:
		return "return false;\n"
	}
}

func cppEmitDeserializeElem(f *pdl.Field, expr string, expandBuiltinHeads bool) string {
	switch f.Type {
	case pdl.TypeU8:
		return "if (!ReadU8(data, len, off, " + expr + ")) return false;\n"
	case pdl.TypeU16:
		return "if (!ReadU16(data, len, off, " + expr + ")) return false;\n"
	case pdl.TypeU32:
		return "if (!ReadU32(data, len, off, " + expr + ")) return false;\n"
	case pdl.TypeU64:
		return "if (!ReadU64(data, len, off, " + expr + ")) return false;\n"
	case pdl.TypeMAC:
		return fmt.Sprintf("if (!ReadBytes(data, len, off, %s, 6)) return false;\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeIPv4:
		return fmt.Sprintf("if (!ReadBytes(data, len, off, %s, 4)) return false;\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeIPv6:
		return fmt.Sprintf("if (!ReadBytes(data, len, off, %s, 16)) return false;\n", cppBytePtrExpr(f, expr, expandBuiltinHeads))
	case pdl.TypeStructRef, pdl.TypeStructArray:
		return "if (!Deserialize(data, len, off, " + expr + ")) return false;\n"
	default:
		return "return false;\n"
	}
}

func cppBytePtrExpr(f *pdl.Field, expr string, expandBuiltinHeads bool) string {
	if expandBuiltinHeads {
		return expr + ".data()"
	}
	switch f.Type {
	case pdl.TypeMAC, pdl.TypeIPv4, pdl.TypeIPv6:
		return expr + ".bytes"
	default:
		return expr
	}
}

func cppIndent(s, p string) string {
	lines := strings.Split(s, "\n")
	for i, ln := range lines {
		if ln == "" {
			continue
		}
		lines[i] = p + ln
	}
	return strings.Join(lines, "\n")
}
